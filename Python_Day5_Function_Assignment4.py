# 4. Accept a name from the user and display that in lower case using lower() function.

# Step 1: Accept a name from the user using the input() function
user_name = input("Enter your name: ")

# Step 2: Convert the entered name to lowercase using the lower() function
lower_case_name = user_name.lower()

# Step 3: Display the name in lowercase
print("Your name in lowercase is:", lower_case_name)

# Answer:-
""" Enter your name: ANCHAL SAINI
Your name in lowercase is: anchal saini """
