# 2. Declare a square() function with one parameter.Then call the function and pass one number and display the square of that number

# Step 1: Declare the function 'square' with one parameter 'num'
def square(num):
    # Step 2: Calculate the square of the number by multiplying it by itself
    result = num * num
    # Step 3: Print the result
    print("The square of", num, "is:", result)

# Step 4: Call the 'square' function and pass a number as an argument
square(5)  # Here, 5 is squared, and the result will be displayed

#Answer:- The square of 5 is: 25
