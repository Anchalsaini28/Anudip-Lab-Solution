# 2.Write a Python program to convert a list to a tuple.

# Input: listx = [5, 10, 7, 4, 15, 3] Output: (5, 10, 7, 4, 15, 3)

# Step 1: Define the list with the given elements
listx = [5, 10, 7, 4, 15, 3]

# Step 2: Convert the list to a tuple using the tuple() function
# The tuple() function takes an iterable (like a list) as an argument and returns a tuple containing the elements of the iterable
converted_tuple = tuple(listx)

# Step 3: Print the result with a message
# This will display the converted tuple
print("The converted tuple is:", converted_tuple)

# Output:-

"""
The converted tuple is: (5, 10, 7, 4, 15, 3)

"""
